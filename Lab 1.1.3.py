import turtle

s = 60
a = [int(i) for i in input()]
turtle.shape('turtle')

nums = [[[(0, 0)], [(1, 0), (2, 90), (1, 90), (2, 90)], [(1, 90)]],
        [[(1, 90)], [(-1 * 2 ** (1 / 2), 45), (2, -45)], [(-2, 0), (0, -90)]],
        [[(0, 0)], [(1, 0), (1, 90), (1 * 2 ** (1 / 2), 45), (1, -135)], [(2, -90), (0, 90)]],
        [[(0, 0)], [(1, 0), (-1 * 2 ** (1 / 2), -45), (1, 45), (-1 * 2 ** (1 / 2), -45)],
         [(1, 45), (2, -90), (0, 90)]],
        [[(0, 90)], [(1, 0), (1, -90), (1, -90), (-2, 0)], [(2, 0), (0, 90)]],
        [[(1, 0)], [(-1, 0), (1, 90), (1, -90), (1, 90), (1, 90)], [(1, 180), (2, -90), (0, 90)]],
        [[(1, 0)], [(-1 * 2 ** (1 / 2), -45), (1, 45), (1, 90), (1, 90), (1, 90)], [(1, 0), (1, 90)]],
        [[(0, 0)], [(1, 0), (-1 * 2 ** (1 / 2), -45), (1, 135)], [(1, -90), (2, -90), (0, 90)]],
        [[(0, 0)], [(1, 0), (1, 90), (1, 90), (1, 90), (-2, 0), (1, 0), (1, 90), (1, 90), (1, 90), (1, 90)],
         [(1, 0), (1, 90)]],
        [[(0, 0)], [(1, 0), (1, 90), (1, 90), (1, 90), (1, 90), (1, 90), (1 * 2 ** (1 / 2), 45)], [(2, 135), (1, 90)]]]


turtle.speed(1)


def draw_alg(alg):
    for v in alg:
        turtle.right(v[1])
        turtle.forward(v[0] * s)


def dram_num(code):
    turtle.penup()
    draw_alg(code[0])
    turtle.pendown()
    draw_alg(code[1])
    turtle.penup()
    draw_alg(code[2])


turtle.penup()
turtle.forward(-300)

for i in a:
    dram_num(nums[i])
    turtle.forward(s)

